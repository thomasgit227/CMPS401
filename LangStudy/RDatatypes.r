variableName <- 10
print( paste("The data type of variableName is", class(variableName) ) )

variableName <- 10+3i
print(paste("The data type of variableName is", class(variableName) ))

variableName <- 2L
print(paste("The data type of variableName is", class(variableName) ))

variableName <- TRUE
print(paste("The data type of variableName is", class(variableName) ))

variableName <- 'a'
print(paste("The data type of variableName is", class(variableName) ))

variableName <- charToRaw("data in here")
print( paste("The data type of variableName is", class(variableName) ))